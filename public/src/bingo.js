$(function() {

    // 빙고 보드 생성
    var cardList = $(".bingo-card");
    var cardItem = "";
    for (var i = 0; i < bingo.questions.length; i++) {
        var content = bingo.questions[i];
        cardItem += 
            "<li class='bingo-card--item'>\
                <a id='question-" + content.num + "' class='card--link' href='#modal' data-num='" + content.num + "' data-question='question-" + content.question + "'>Modal" + content.num + "</a>\
            </li>";                
    }
    cardList.append(cardItem);

    // 팀 선택 라디오 생성
    var btnSelectTeam = $("#radio-btn-team");
    var teamItem = "";
    for (var i = 0; i < team.teams.length; i++) {       
        var a = team.teams[i];
        console.log(a);
        teamItem +=
            "<input id='radio-" + a.name + "' type='radio' name='team' value='"+ a.name + "'/><label for='radio-" + a.name + "'>" + a.name + "</label>";
    }
    btnSelectTeam.append(teamItem);
    
    $('.modal').modal({
        num: -1,
        ready: function(modal, trigger) {
            var question = trigger.data('question');
            num = trigger.data('num');
            modal.find('.modal-content').text(question);
        },
        complete: function() {
            var team = $(this).find('input[name=team]:checked').val();
            switch(team) {
                case "A" :
                    $("#question-" + num).css('background-color', '#53777A');
                    break;
                case "B" :
                    $("#question-" + num).css('background-color', '#C02942');
                    break;
                case "C" :
                    $("#question-" + num).css('background-color', '#ECD078');
                    break;
                default :
                    break;
            }
        }
    });
});